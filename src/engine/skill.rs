#[derive(PartialEq, Eq, PartialOrd, Ord)]
pub struct Skill {
    pub name: &'static str,
    pub id: u16,
}

#[derive(Clone, Copy, PartialEq)]
pub enum Gamemode {
    Vanilla,
    HardType,
}

impl Skill {
    const fn new(name: &'static str, id: u16) -> Self {
        Self { name, id }
    }

    pub const fn table(gamemode: Gamemode) -> &'static [Skill] {
        match gamemode {
            Gamemode::Vanilla => SKILL_TABLE,
            Gamemode::HardType => SKILL_TABLE_HT,
        }
    }

    pub const fn table_size(gamemode: Gamemode) -> usize {
        Self::table(gamemode).len()
    }

    pub fn get(gamemode: Gamemode, id: usize) -> Result<&'static Self, String> {
        if id < Self::table_size(gamemode) {
            Ok(&Self::table(gamemode)[id])
        } else {
            Err("Invalid skill ID".to_string())
        }
    }
}

